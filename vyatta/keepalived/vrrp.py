from typing import Dict


class VrrpGroup:
    """
    Simple VRRP group representation

    Used to represent the keepalived config for each individual
    VRRP group
    """

    def __init__(self, name: str, delay: str, group_config: Dict):
        """
        Constructor for the class

        Arguments:
            name (str):
                Name of the interface the VRRP group is configured on
            delay (str):
                Start delay configured for the interface
            group_config (Dict):
                YANG Dictionary for the group's config
        """
        # Default values from existing code required for minimal
        # config
        self._keepalived_name = group_config
        if "priority" not in self._keepalived_name:
            self._keepalived_name["priority"] = 100
        if "advertise-interval" not in self._keepalived_name \
                and self._keepalived_name["version"] == 2:
            self._keepalived_name["adv"] = 1
        else:
            self._keepalived_name["adv"] = \
                group_config["advertise-interval"]
            del(self._keepalived_name["advertise-interval"])

        # Values outwith the dictionary
        self._keepalived_name["intf"] = name
        self._keepalived_name["delay"] = delay
        self._keepalived_name["state"] = "BACKUP"

        # Autogenerated values from minimal config
        self._keepalived_name["vrid"] = group_config["tagnode"]
        self._keepalived_name["accept"] = group_config["accept"]

        self._keepalived_name["vips"] = "\n".join(
            group_config["virtual-address"])
        del(self._keepalived_name["virtual-address"])

        # Template required for minimal config
        self._template = """
vrrp_instance {instance} {{
    state {state}
    interface {intf}
    virtual_router_id {tagnode}
    version {version}
    start_delay {delay}
    priority {priority}
    advert_int {adv}
    virtual_ipaddress {{
        {vips}
    }}
"""

        # Generate instance name (TODO change to f-string with python 3.7)
        self._instance = "vyatta-{intf}-{vrid}".format(
            intf=name, vrid=group_config["tagnode"]
        )
        self._keepalived_name["instance"] = self._instance

        self._template += "}}"

    @property
    def instance_name(self):
        """Name of this group in the config file"""
        return self._instance

    def __repr__(self):
        completed_config = self._template.format(
            **self._keepalived_name
        )
        return completed_config
